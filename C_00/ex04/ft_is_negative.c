#include <unistd.h>

void	ft_is_negative(int n)
{
	char	flag;

	flag = 'P';
	if (n >= 0)
	{
		write(1, &flag, 1);
	}
	else
	{
		flag = 'N';
		write(1, &flag, 1);
	}
}
