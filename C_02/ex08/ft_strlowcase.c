char	*ft_strlowcase(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 32;
	while (str[i])
	{
		if (str[i] >= 65 && str[i] <= 90)
		{
			str[i] = str[i] + j;
		}
		i++;
	}
	return (str);
}
